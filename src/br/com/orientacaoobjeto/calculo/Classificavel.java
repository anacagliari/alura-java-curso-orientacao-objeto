package br.com.orientacaoobjeto.calculo;

// Em Java, interfaces são uma forma de definir um contrato que as classes devem seguir, 
// sendo que ele define quais métodos devem ser implementados pelas classes que o implementarem.
// Interfaces permitem que diferentes classes possam ser tratadas de maneira padronizada, 
// via polimorfismo, tornando assim o código fácil de estender com novos comportamentos.
public interface Classificavel {
    int getClassificacao();
}
